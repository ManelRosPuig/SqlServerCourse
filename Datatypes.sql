--------------------------------------------
-- TEXT
--------------------------------------------

-- Store datatypes of a fixed width.
-- It can be both characters or numbers.
CHAR

-- Store datatypes of a non-fixed width.
VARCHAR

-- Store text datatypes.
TEXT

-- Store datatypes of a fixed width.
-- If the characters are not 100% filled, 
-- it reuses those non-used free spaces.
NCHAR

-- Store datatypes of a non-fixed width.
-- If the characters are not 100% filled, 
-- it reuses those non-used free spaces.
NVARCHAR

--------------------------------------------

--------------------------------------------
-- NUMBERS
--------------------------------------------

-- Store 2 values: 1 or 0.
BIT

-- Store values from -2.147.483.648 to 2.147.483.647.
INT

-- Store values from -9.223.372.036.854.775.808 to 9.223.372.036.854.775.807.
BIGINT

-- Store values from -10^38 + 1 to 10^38 -1.
DECIMAL

-- Same as DECIMAL.
NUMERIC

-- Same as BIGINT.
MONEY

-- Store values from -1.79E + 308 to 1.79E + 308.
FLOAT

